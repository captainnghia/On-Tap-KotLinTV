@Composable
fun Greeting(name: String, modifier: Modifier = Modifier) {
    var countTest = 0
    // var count by remember { mutableStateOf(0) }
    var count by rememberSaveable { mutableStateOf(0) }
    var history = rememberSaveable { mutableStateListOf<Int>() }

    Log.i("TAG", "Greeting $count $countTest")
    Column {
        Text("$count")
        Button(onClick = {
            count++
            countTest++
            history.add(count)
            Log.i("TAG", "onClick $count $countTest")
        }) {
            Text(text = "+")
        }
        Text("History:")
        for(i in history.toList()) {
            Text(text = "$i")
        }
    }
}

